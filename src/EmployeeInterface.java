    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        if(size <= 9){
        Employee temp;
        String nm, type = null;
        int hours;
        double rate;
        try {
            nm = txtname.getText();
            hours = Integer.parseInt(txthours.getText());
            rate = Double.parseDouble(txtrate.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Must fill out form correctly");
            return;
        }
        if (btnfull.isSelected()) {
            temp = new FullTimeEmployee();
        } else {
            temp = new PartTimeEmployee();
        }
        if (temp.setName(nm) && temp.setHours(hours) && temp.setRate(rate)) {
            double pay;
            pay = temp.getPay();
            emp[size] = temp;
            table.setValueAt(temp.getName(), size, 0);
            table.setValueAt(nf.format(pay), size, 1);
            size++;
            txttotalpay.setText(nf.format(Employee.getTotalPay()));
            clearform();
            return;
        }

        String error = "ERROR\n=====\n";
        if (temp.setName(nm) == false) {
            error += "Name: " + Employee.getNameRules() + "\n";
        }
        if (temp.setHours(hours) == false) {
            error += "Hours: " + Employee.getHoursRules() + "\n";
        }
        if (temp.setRate(rate) == false) {
            error += "Rate: " + Employee.getRateRules() + "\n";
        }
        JOptionPane.showMessageDialog(this, error);
        } 
        else JOptionPane.showMessageDialog(this, "Employees cannot exceed 10.");
    }//GEN-LAST:event_btnaddActionPerformed

